package Secao10;

public class Aula97 {

	public static void main(String[] args) {

		/*
			Os VETORES são uma estrutura de código encadeada.
			No java todo vetor é um Objeto, eles funcionam
			como listas de ponteiros para valores dentro da
			memória HEAP.
		*/
		
		// Declarando um vetor
		int valores[] = new int[5];
		
		// Declarando valores para cada índice do vetor.
		valores[0] = 10;
		valores[1] = 20;
		valores[2] = 30;
		valores[3] = 40;
		valores[4] = 50;
		
	}

}
